"""
Aircraft Configuration Module

This module defines the AIRCRAFT_CONFIG dictionary, which contains configuration parameters
for different aircraft models and modifications.

Each aircraft configuration is stored as an AircraftConfig dataclass instance with 35 named
fields. The dataclass supports:
  - Named attribute access:  config.MTOW, config.wing_area
  - Numeric index access:    config[21]  (backward-compatible with legacy tuple layout)
  - Iteration / unpacking:   s, b, e, h, ... = config
  - len():                   len(config) == 35
  - tuple() conversion:      tuple(config) returns a plain 35-element tuple
"""

from dataclasses import dataclass, fields, astuple
import logging as _logging

_log = _logging.getLogger(__name__)


@dataclass(slots=True)
class AircraftConfig:
    """Aircraft configuration with 35 parameters. Supports index access for backward compat."""

    # Geometry
    wing_area: float        # 0  - Wing area (ft²)
    span: float             # 1  - Wing span (ft)
    oswald: float           # 2  - Oswald efficiency factor
    winglet_height: float   # 3  - Winglet height (ft)
    sweep_25c: float        # 4  - Wing sweep at 25% chord (degrees)

    # Propulsion
    SFC: float              # 5  - Specific Fuel Consumption (lb/hr/lb)
    engines: int            # 6  - Number of engines
    thrust_mult: float      # 7  - Thrust multiplier

    # Performance limits
    ceiling: float          # 8  - Service ceiling (ft)

    # Lift
    CL0: float              # 9  - Zero-lift coefficient
    CLA: float              # 10 - Lift curve slope (1/rad)

    # Drag
    cdo: float              # 11 - Zero-lift drag coefficient
    dcdo_flap1: float       # 12 - ΔCd₀ takeoff flaps 15
    dcdo_flap2: float       # 13 - ΔCd₀ takeoff flaps 30
    dcdo_flap3: float       # 14 - ΔCd₀ ground flaps/spoilers 40
    dcdo_gear: float        # 15 - ΔCd₀ landing gear

    # Ground friction
    mu_to: float            # 16 - Rolling friction (takeoff)
    mu_lnd: float           # 17 - Rolling friction (landing)

    # Weights (lb)
    bew: float              # 18 - Basic Empty Weight
    MZFW: float             # 19 - Maximum Zero Fuel Weight
    MRW: float              # 20 - Maximum Ramp Weight
    MTOW: float             # 21 - Maximum Takeoff Weight
    max_fuel: float         # 22 - Maximum fuel capacity

    # Fuel defaults (lb)
    taxi_fuel: float        # 23 - Default taxi fuel
    reserve_fuel: float     # 24 - Default reserve fuel

    # Speed limits
    mmo: float              # 25 - Maximum operating Mach number
    VMO: float              # 26 - Maximum operating speed (kts)

    # Cl limits
    Clmax: float            # 27 - Max CL clean
    Clmax_flaps15: float    # 28 - Max CL flaps 15
    Clmax_flaps40: float    # 29 - Max CL flaps 40

    # Climb/descent schedule
    M_climb: float          # 30 - Climb Mach number
    v_climb: float          # 31 - Climb speed (kts)
    roc_min: float          # 32 - Minimum rate of climb (ft/min)
    M_descent: float        # 33 - Descent Mach number
    v_descent: float        # 34 - Descent speed (kts)

    # ── index / iteration helpers (backward-compatible with tuple access) ──

    _FIELDS = None  # class-level cache, set after class creation

    def __getitem__(self, index):
        return astuple(self)[index]

    def __len__(self):
        return 35

    def __iter__(self):
        return iter(astuple(self))

    @classmethod
    def from_tuple(cls, t):
        """Construct from a 35-element tuple/list (migration helper)."""
        return cls(*t)

    def validate(self, label: str = ""):
        """Raise ValueError if any physical constraint is violated."""
        errors = []
        pfx = f"[{label}] " if label else ""

        # Positive geometry
        if self.wing_area <= 0:
            errors.append(f"{pfx}wing_area must be > 0, got {self.wing_area}")
        if self.span <= 0:
            errors.append(f"{pfx}span must be > 0, got {self.span}")
        if not (0 < self.oswald <= 1.5):
            errors.append(f"{pfx}oswald must be in (0, 1.5], got {self.oswald}")

        # Propulsion
        if self.engines < 1:
            errors.append(f"{pfx}engines must be >= 1, got {self.engines}")
        if self.thrust_mult <= 0:
            errors.append(f"{pfx}thrust_mult must be > 0, got {self.thrust_mult}")
        if self.SFC <= 0:
            errors.append(f"{pfx}SFC must be > 0, got {self.SFC}")

        # Weights: BEW < MZFW <= MTOW <= MRW (allow MTOW == MRW for some types)
        if self.bew <= 0:
            errors.append(f"{pfx}bew must be > 0, got {self.bew}")
        if self.MTOW <= 0:
            errors.append(f"{pfx}MTOW must be > 0, got {self.MTOW}")
        if self.bew >= self.MTOW:
            errors.append(f"{pfx}bew ({self.bew}) must be < MTOW ({self.MTOW})")
        if self.MZFW > self.MTOW:
            errors.append(f"{pfx}MZFW ({self.MZFW}) must be <= MTOW ({self.MTOW})")
        if self.max_fuel <= 0:
            errors.append(f"{pfx}max_fuel must be > 0, got {self.max_fuel}")

        # Speed limits
        if not (0 < self.mmo <= 1.0):
            errors.append(f"{pfx}mmo must be in (0, 1.0], got {self.mmo}")
        if self.VMO <= 0:
            errors.append(f"{pfx}VMO must be > 0, got {self.VMO}")

        # Ceiling
        if self.ceiling <= 0:
            errors.append(f"{pfx}ceiling must be > 0, got {self.ceiling}")

        if errors:
            raise ValueError("\n".join(errors))


